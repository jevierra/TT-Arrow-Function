const name = "Jeff";
/*

Function declaration 
function sayName() {
    const message = "My name is " + name;
    console.log(message);
}


 Function expression 
const sayName = function() {
    const message = "My name is " + name;
    console.log(message);
}
*/

/* Arrow Function */
const sayName = () => {
    const message = "My name is " + name;
    console.log(message);
}



sayName();
